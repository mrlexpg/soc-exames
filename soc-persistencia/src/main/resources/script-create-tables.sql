CREATE TABLE CONVENIO (
     ID_CONVENIO BIGINT NOT NULL IDENTITY,
     NM_CONVENIO VARCHAR(50),
     PRIMARY KEY(ID_CONVENIO)
);

CREATE TABLE MEDICO (
     ID_MEDICO BIGINT NOT NULL IDENTITY,
     NM_MEDICO VARCHAR(40),
     PRIMARY KEY(ID_MEDICO)
);

CREATE TABLE EXAME (
     ID_CONSULTA BIGINT NOT NULL IDENTITY,
     DS_TIPO_EXAME VARCHAR(40),
     DT_EXAME DATE,
     DS_RESULTADO VARCHAR(100),
     PRIMARY KEY(ID_CONSULTA)
);

CREATE TABLE PACIENTE (
     ID_PACIENTE BIGINT NOT NULL IDENTITY,
     NM_PACIENTE VARCHAR(50),
     SG_SEXO VARCHAR(1),
     DT_NASCIMENTO DATETIME,
     ID_CONVENIO BIGINT,
     DS_ENDERECO VARCHAR(50),
     NU_RG INT,
     DS_ESTADO_CIVIL VARCHAR(10),
     CD_DDD INT,
     NU_TELEFONE INT,
     PRIMARY KEY(ID_PACIENTE),
     FOREIGN KEY(ID_CONVENIO)
     REFERENCES CONVENIO(ID_CONVENIO)
     ON DELETE NO ACTION
     ON UPDATE NO ACTION
);

CREATE TABLE CONSULTA (
     ID_CONSULTA BIGINT NOT NULL IDENTITY,
     ID_PACIENTE BIGINT,
     ID_MEDICO BIGINT,
     DT_CONSULTA DATETIME,
     DS_DIAGNOSTICO VARCHAR(250),     
     PRIMARY KEY(ID_CONSULTA),
     FOREIGN KEY(ID_PACIENTE)
     REFERENCES PACIENTE(ID_PACIENTE)
     ON DELETE NO ACTION
     ON UPDATE NO ACTION,
     FOREIGN KEY(ID_MEDICO)
     REFERENCES MEDICO(ID_MEDICO)
     ON DELETE NO ACTION
     ON UPDATE NO ACTION
);

